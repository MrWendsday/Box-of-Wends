package civichall.collections.webapp.main;

public class ProductSize {

	private int id;
	private String size;
	
	public ProductSize(int id, String size ) {
		super();
		this.id = id;
		this.size = size;
	}
	
	public int getId() {
		return id;
	}
	public String getSize() {
		return size;
	}
}
